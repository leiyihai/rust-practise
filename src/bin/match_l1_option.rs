// 【题目需求】
// 给定一个 Option<i32> 类型的变量，使用完整的 match 表达式
// 分别处理有值（Some）和无值（None）两种情况，并打印对应信息。
//
// 【具体要求】
// 1. 定义一个 Option<i32> 变量，例如 let num_opt = Some(66);
// 2. 用 match 穷尽所有分支（Some 和 None）
// 3. 在 Some 分支中解构出内部值并打印
// 4. 在 None 分支中打印"没有值"
// 5. 不允许使用 if let 语法糖
//
// 【考察点】
// - match 穷尽性：编译器强制检查所有变体是否被覆盖
// - 枚举变体解构：Some(v) 绑定内部值到变量 v
// - 少写任何一个分支都会编译报错，体会 Rust 的模式匹配安全机制
//
// 【代码骨架】
fn main() {
    let opt: Option<i32> = Some(40);
    match opt {
        Some(v) => println!("{}", v),
        None => println!("没有值"),
    }
}
