// 【题目需求】
// 输入年龄，分段输出：
//   0~12  "儿童"
//   13~17 "青少年"
//   18 以上 "成年"
// 将 match 作为表达式，把结果赋值给变量并打印。
//
// 【具体要求】
// 1. 定义 let age = 23;
// 2. 使用 match 表达式，返回值赋给变量 tip
// 3. 区间匹配使用 ..= 语法（闭区间）
// 4. 兜底分支 _ => "未知"
// 5. 用 println!("{}", tip); 打印结果
//
// 【考察点】
// - 区间模式：0..=12 表示 [0, 12] 闭区间
// - match 是表达式不是语句：可以直接把匹配结果赋值给变量
// - 每个分支返回的值类型必须一致（这里都是 &str）
//
// 【代码骨架】
fn main() {
    let age = 23;
    let tip = match age {
        0..=12 => "儿童",
        13..=17 => "青少年",
        n if n >= 18 && n < 60 => "成年",
        _ => "老年",
    };
    println!("{}", tip);
}
