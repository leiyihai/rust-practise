// 【具体要求】
// 1. 定义 let num: u8 = 150;
// 2. 使用 match 配合 @ 绑定：
//    - n @ 0..=99   → 打印"0~99 之间: {n}"
//    - n @ 100..=199 → 打印"100~199 之间: {n}"
//    - n @ 200..=255 → 打印"200~255 之间: {n}"
// 3. 体会 @ 的作用：既限定了范围，又拿到了原始值
