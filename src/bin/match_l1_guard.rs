//
// 【题目需求】
// 一个 Option 包裹分数（u32），在 Some 分支中通过守卫条件判断：
// 数值 ≥ 60 打印"及格"，否则打印"不及格"；None 打印"无成绩"。
//
// 【具体要求】
// 1. 定义 let score = Some(72);
// 2. match score，在 Some(val) 分支中使用 if 守卫
// 3. Some(val) if val >= 60 → 打印"及格，分数：{val}"
// 4. Some(val) → 打印"不及格，分数：{val}"
// 5. None → 打印"无成绩"
//
// 【考察点】
// - 模式 if 条件：在模式匹配之后再进行二次过滤
// - 守卫不会改变模式结构，只是额外加一层判断
// - 注意分支顺序：带守卫的分支必须在不带守卫的分支之前（如果有重叠）
// - 本例中 Some(val) if val >= 60 和 Some(val) 是包含关系，守卫分支持更严格的条件
//
// 【代码骨架】
fn main() {
    let score: Option<u32> = Some(72);
    match score {
        Some(val) if val >= 60 => { println!("及格，分数：{}", val) },
        Some(val) => println!("不及格，分数：{}", val),
        None => println!("无成绩")
    }
}
