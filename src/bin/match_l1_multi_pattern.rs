// 【题目需求】
// 输入单个字符，用 match 一次性匹配 a/A/b/B，统一输出「字母 AB 类」；
// 数字 0~9 输出「数字」；其余输出「其他」。
//
// 【具体要求】
// 1. 定义一个 char 变量，例如 let c = 'C';
// 2. 使用 | 运算符将多个模式合并到同一个分支
// 3. 分支 1：'a' | 'A' | 'b' | 'B' → 打印"字母 AB 类"
// 4. 分支 2：'0'..='9' → 打印"数字"
// 5. 分支 3：_ → 打印"其他"
//
// 【考察点】
// - 模式1 | 模式2：同一分支命中多个值，一行替代多行重复代码
// - 与范围模式 ..= 的区别：| 用于离散值，..= 用于连续区间
// - 减少代码重复，提高可读性
//
// 【代码骨架】
fn main() {
    let c = 'b';
    match c {
        'a' | 'A' | 'b' | 'B' => println!("字母 AB 类"),
        '0'..='9' => println!("数字"),
        _ => println!("其他"),
    }
    // match c {
    //     'a' | 'A' | 'b' | 'B' | '0'..='9' => println!("字母 AB 类 + 数字"),
    //     _ => println!("其他"),
    // }
}
